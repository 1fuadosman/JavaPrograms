public class ReferenceVObjectVInstanceVClass {
    /*

    **Reference vs Object vs Instance vs Class
    *
    - Lets use the analogy of building a house to understand Classes
    - A class is basically a blueprint for a house, using the blueprint(plans) we can
    build as many houses as we like based on those plans.
    - Each house you build(in other words instantiate using the new operator) is an object
    also known as an instance
    - Each house you build has an address(a physical location). In other words if you want to tell
    someone where you live, you give them your address(perhaps written on a piece of paper). This is
    known as a reference.
    - You can copy that reference as many times as you like but there is still just one house.
    In other words we are copying the paper that has dress on it not the house itself
    - We can pass references as parameters to constructor and methods

     */
}

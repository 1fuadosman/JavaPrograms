package com.OOPS6._63to69Inheritance.Lect65to69;

public class thisVsuper {
    /*
    - The keyword super is used to access/call the parent class variables
    or methods.
    - The keyword this is used to call the current class variables and methods.This is
    required when we have a parameter with the same name as an instance variable(field).
    - NOTE: We can use both of them anywhere in a class except static areas(the static block or
    static method). Any attempt to do so will lead to compile-time error
    (more on static later in the course).


    - The keyword this is commonly used with constructors and setters, and optionally in getters
    (easier for beginners).
    - The keyword super is commonly used with method overriding, when we call a method with the same
    name from the parent class.



     */
}
